public class TotalAreaSalon {
    private int total;
    private int area_salon;

    public TotalAreaSalon(int total, int area_salon) {
        this.total = total;
        this.area_salon = area_salon;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getArea_salon() {
        return area_salon;
    }

    public void setArea_salon(int area_salon) {
        this.area_salon = area_salon;
    }
    
    
}
